create or replace function public.claim_trial(
  p_user_id uuid,
  p_email text,
  p_ip_hash text,
  p_duration_minutes integer default 60
)
returns table(subscription_id uuid, trial_ends_at timestamptz)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_plan_id uuid;
  v_subscription_id uuid;
  v_ends_at timestamptz;
begin
  if exists (select 1 from public.trial_claims where user_id = p_user_id) then
    raise exception 'trial_already_used';
  end if;

  if exists (
    select 1 from public.subscriptions
    where user_id = p_user_id and status = 'active' and ends_at > now()
  ) then
    raise exception 'active_subscription';
  end if;

  select id into v_plan_id from public.plans where slug = 'teste-1h' limit 1;
  v_ends_at := now() + make_interval(mins => greatest(p_duration_minutes, 1));

  insert into public.trial_claims(user_id, email, ip_hash, claimed_at)
  values (p_user_id, p_email, p_ip_hash, now());

  insert into public.subscriptions(
    user_id, plan_id, payment_id, status, is_trial, starts_at, ends_at, updated_at
  ) values (
    p_user_id, v_plan_id, null, 'active', true, now(), v_ends_at, now()
  )
  on conflict (user_id) do update set
    plan_id = excluded.plan_id,
    payment_id = null,
    status = 'active',
    is_trial = true,
    starts_at = excluded.starts_at,
    ends_at = excluded.ends_at,
    updated_at = now()
  returning id into v_subscription_id;

  return query select v_subscription_id, v_ends_at;
end;
$$;

revoke all on function public.claim_trial(uuid, text, text, integer) from public, anon, authenticated;
grant execute on function public.claim_trial(uuid, text, text, integer) to service_role;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer set search_path = public
as $$
begin
  insert into public.profiles (id, full_name)
  values (new.id, coalesce(new.raw_user_meta_data->>'full_name', ''))
  on conflict (id) do nothing;
  return new;
end;
$$;

drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
after insert on auth.users
for each row execute procedure public.handle_new_user();


create or replace function public.current_user_role()
returns text
language sql
stable
security definer
set search_path = public
as $$
  select role from public.profiles where id = auth.uid();
$$;

create or replace function public.is_admin()
returns boolean
language sql
stable
security definer
set search_path = public
as $$
  select exists (
    select 1 from public.profiles
    where id = auth.uid() and role = 'admin'
  );
$$;


