  on public.payments(provider, provider_transaction_id)
  where provider_transaction_id is not null;
create index if not exists payments_user_created_idx on public.payments(user_id, created_at desc);
create index if not exists payments_status_paid_idx on public.payments(status, paid_at desc);

create table if not exists public.subscriptions (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null unique references public.profiles(id) on delete cascade,
  plan_id uuid references public.plans(id),
  payment_id uuid references public.payments(id),
  status text not null default 'active' check (status in ('active','paused','cancelled','expired')),
  is_trial boolean not null default false,
  starts_at timestamptz not null,
  ends_at timestamptz not null,
  updated_at timestamptz not null default now(),
  constraint subscriptions_valid_period check (ends_at > starts_at)
);
create index if not exists subscriptions_active_idx on public.subscriptions(status, ends_at desc);

create table if not exists public.access_tokens (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references public.profiles(id) on delete cascade,
  subscription_id uuid references public.subscriptions(id) on delete cascade,
  token_hash text not null unique,
  kind text not null check (kind in ('trial','subscription')),
  created_at timestamptz not null default now(),
  last_used_at timestamptz,
  revoked_at timestamptz
);
alter table public.access_tokens add column if not exists last_used_at timestamptz;
create index if not exists access_tokens_user_idx on public.access_tokens(user_id, created_at desc);

create table if not exists public.trial_claims (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null unique references public.profiles(id) on delete cascade,
