create extension if not exists pgcrypto;

create table if not exists public.profiles (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text,
  phone text,
  role text not null default 'customer' check (role in ('customer','support','admin')),
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.plans (
  id uuid primary key default gen_random_uuid(),
  name text not null,
  slug text not null unique,
  description text,
  price_cents integer not null check (price_cents >= 0),
  duration_days integer not null default 30 check (duration_days >= 0),
  features jsonb not null default '[]'::jsonb,
  active boolean not null default true,
  created_at timestamptz not null default now()
);

create table if not exists public.payments (
  id uuid primary key default gen_random_uuid(),
  user_id uuid not null references public.profiles(id) on delete cascade,
  plan_id uuid not null references public.plans(id),
  amount_cents integer not null check (amount_cents > 0),
  currency text not null default 'BRL',
  provider text not null,
  provider_transaction_id text,
  status text not null default 'pending' check (status in ('pending','processing','paid','approved','completed','confirmed','failed','cancelled','expired','refunded')),
  qr_code text,
  copy_paste text,
  expires_at timestamptz,
  provider_response jsonb,
  webhook_payload jsonb,
  paid_at timestamptz,
  created_at timestamptz not null default now()
);

